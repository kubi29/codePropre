package ex1;

public class Calcul {

	/** permet d'effectuer une ddition
	 * @param a
	 * @param b
	 * @return
	 */
	public int additionner(int a, int b){
		return a+b;
	}
	
	/** permet d'effectuer une soustraction
	 * @param a
	 * @param b
	 * @return
	 */
	public int soustraire(int a, int b){
		return a-b;
	}
}
